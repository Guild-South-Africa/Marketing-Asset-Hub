export function getAssetById(id: string): CampaignAsset | undefined {
  return assetRegistry.find((a) => a.id === id)
}

export function getAssetsByPhase(phase: CampaignAsset['phase']): CampaignAsset[] {
  return assetRegistry.filter((a) => a.phase === phase)
}

export function getUniqueTemplates(): string[] {
  return [...new Set(assetRegistry.map((a) => a.templateSlug))]
}

